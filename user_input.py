# Prompts the user to enter their name and store it in the variable 'name'
name=input("What is your name")
# Prompts the user to enter their age and store it in the variable 'age'
age=int(input("How old are you"))
# Prompts the user to enter their location and store it in the variable 'location'
location=input("Where do you live?")
# Print a personalized message that includes the user's name, age, and location.
print(f"Hello {name}, you are {age} years old and you live in {location}") 
